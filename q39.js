//making function 
function city_country(city, country) {
    return ("".concat(city, ", ").concat(country));
}
//calling function with adding value
var city1 = city_country("Karachi", "Pakistan");
console.log(city1);
var city2 = city_country("Istanbul", "Turkey");
console.log(city2);
var city3 = city_country("Sharjah", "Dubai");
console.log(city3);
var city4 = city_country("Tokyo", "Japan");
console.log(city4);
